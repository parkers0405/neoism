macro_rules! route {
    ($id:literal, $language_id:literal, [$($extension:literal),* $(,)?], [$($pattern:literal),* $(,)?]) => {
        LanguageRoute {
            id: $id,
            document_language_id: $language_id,
            extensions: &[$($extension),*],
            filename_patterns: &[$($pattern),*],
        }
    };
}

macro_rules! stdio {
    ($($part:literal),+ $(,)?) => {
        LspTransportSpec::Stdio { command: &[$($part),+] }
    };
}

macro_rules! catalog {
    ($package_id:literal => $executable:literal $(,)?) => {
        &[CatalogPackageSpec {
            package_id: $package_id,
            executable: $executable,
        }]
    };
}

